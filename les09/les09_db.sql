-- 1. Создайте хранимую функцию hello(), которая будет возвращать приветствие, в зависимости от текущего времени суток. С 6:00 до 12:00 функция должна возвращать фразу "Доброе утро", с 12:00 до 18:00 функция должна возвращать фразу "Добрый день", с 18:00 до 00:00 — "Добрый вечер", с 00:00 до 6:00 — "Доброй ночи".

DROP FUNCTION IF EXISTS hello;

DELIMITER //
CREATE FUNCTION hello()
RETURNS VARCHAR(50) NOT DETERMINISTIC
READS SQL DATA
BEGIN
  CASE
    WHEN CURRENT_TIME() BETWEEN TIME("06:00:00") AND TIME("12:00:00") THEN 
      RETURN "Доброе утро";
    WHEN CURRENT_TIME() BETWEEN TIME("12:00:00") AND TIME("18:00:00") THEN
      RETURN "Добрый день";
    WHEN CURRENT_TIME() BETWEEN TIME("18:00:00") AND TIME("23:59:59") THEN
      RETURN "Добрый вечер";
    WHEN CURRENT_TIME() BETWEEN TIME("00:00:00") AND TIME("06:00:00") THEN
      RETURN "Доброй ночи";
  END CASE;
END//

DELIMITER ;

